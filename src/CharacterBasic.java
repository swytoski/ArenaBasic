public class CharacterBasic {

	/**
	 * this is all the stats that are used for characters and monsters in the basic game.
	 * 
	 */
	private int Strength;
	private int Agility;
	private int Intelegence;
	private int Atk;
	private int def;
	private int MAtk;
	private int MDef;
	private int HP;
	private int MP;
	
	private int XP;
	private int Level;
	
	/**
	 * @return the strength
	 */
	public int getStrength() {
		return Strength;
	}
	/**
	 * @param strength the strength to set
	 */
	public void setStrength(int strength) {
		Strength = strength;
	}
	/**
	 * @return the agility
	 */
	public int getAgility() {
		return Agility;
	}
	/**
	 * @param agility the agility to set
	 */
	public void setAgility(int agility) {
		Agility = agility;
	}
	/**
	 * @return the intelegence
	 */
	public int getIntelegence() {
		return Intelegence;
	}
	/**
	 * @param intelegence the intelegence to set
	 */
	public void setIntelegence(int intelegence) {
		Intelegence = intelegence;
	}
	/**
	 * @return the atk
	 */
	public int getAtk() {
		return Atk;
	}
	/**
	 * @param atk the atk to set
	 */
	public void setAtk(int atk) {
		Atk = atk;
	}
	/**
	 * @return the def
	 */
	public int getDef() {
		return def;
	}
	/**
	 * @param def the def to set
	 */
	public void setDef(int def) {
		this.def = def;
	}
	/**
	 * @return the mAtk
	 */
	public int getMAtk() {
		return MAtk;
	}
	/**
	 * @param mAtk the mAtk to set
	 */
	public void setMAtk(int mAtk) {
		MAtk = mAtk;
	}
	/**
	 * @return the mDef
	 */
	public int getMDef() {
		return MDef;
	}
	/**
	 * @param mDef the mDef to set
	 */
	public void setMDef(int mDef) {
		MDef = mDef;
	}
	/**
	 * @return the hP
	 */
	public int getHP() {
		return HP;
	}
	/**
	 * @param hP the hP to set
	 */
	public void setHP(int hP) {
		HP = hP;
	}
	/**
	 * @return the mP
	 */
	public int getMP() {
		return MP;
	}
	/**
	 * @param mP the mP to set
	 */
	public void setMP(int mP) {
		MP = mP;
	}
	/**
	 * @return the xP
	 */
	public int getXP() {
		return XP;
	}
	/**
	 * @param xP the xP to set
	 */
	public void setXP(int xP) {
		XP = xP;
	}
	/**
	 * @return the level
	 */
	public int getLevel() {
		return Level;
	}
	/**
	 * @param level the level to set
	 */
	public void setLevel(int level) {
		Level = level;
	}
	
}
