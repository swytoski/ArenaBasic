
public class ItemsBasic {

	private String Name;
	private int Effect;

	/**
	 * @return the name
	 */
	public String getName() {
		return Name;
	}

	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		Name = name;
	}

	/**
	 * @return the effect
	 */
	public int getEffect() {
		return Effect;
	}

	/**
	 * @param effect the effect to set
	 */
	public void setEffect(int effect) {
		Effect = effect;
	}
	
	
	
}
